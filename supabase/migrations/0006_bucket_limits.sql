-- 0006_bucket_limits.sql — «Кадр» — серверные лимиты бакета + чистка мусорных photos
-- Источник: SPECIFICATION.md §5 (Storage: лимит ≤12 МБ, единый формат .jpg,
--           минимизация данных), §9 п.11 (загрузка >12 МБ или не-изображение —
--           СЕРВЕР валидирует content-type/размер), §6.3 (upload-url резервирует
--           photos с uploaded=false до фактической загрузки).
--           Опирается на: 0001_init.sql (bucket event-photos, таблица photos),
--           0004/0005 (образец идемпотентной (пере)регистрации cron-задач).
--
-- Закрываются находки бэклога качества (обе MEDIUM):
--
--   M-§9п11. Бакет event-photos создан в 0001 БЕЗ ограничений: file_size_limit и
--            allowed_mime_types были NULL. Клиент ресайзит/режектит, но злоумышленник
--            может PUT-нуть не-изображение или файл >12 МБ напрямую на подписанный
--            upload-URL, минуя клиентскую валидацию. Фикс: ставим жёсткие лимиты на
--            сам бакет. storage-api проверяет их при загрузке (в т.ч. через signed
--            upload URL: см. ПРОВЕРКУ ниже и отчёт).
--            - file_size_limit = 12 МБ = 12 * 1024 * 1024 = 12582912 байт (§5);
--            - allowed_mime_types = ['image/jpeg'] — СТРОГО один формат. §5 хранит
--              кадр как .jpg, превью тоже .jpg → минимизация данных, один формат.
--              png/webp намеренно НЕ разрешаем.
--
--   M-резервы. При «Переснять» клиент бросает зарезервированный photo_id
--            (photos.uploaded=false), строка остаётся в БД навсегда → накопление
--            мусора. Фикс — cron-чистка брошенных резервов (см. ниже). Явное удаление
--            резерва клиентом — Edge Function discard-photo (вне миграции).
--
-- Идемпотентность: update бакета безопасен при повторе (тот же результат); cron —
-- unschedule перед schedule (как 0004/0005). Новых таблиц нет → RLS не требуется.
-- ============================================================================

-- ---------------------------------------------------------------------------
-- M-§9п11 · Лимиты бакета event-photos (серверная валидация размера/типа)
-- ---------------------------------------------------------------------------
-- storage.buckets.file_size_limit — bigint (байты), allowed_mime_types — text[].
-- update по фиксированному id='event-photos' идемпотентен.
update storage.buckets
set file_size_limit    = 12582912,            -- 12 МБ (§5), в байтах
    allowed_mime_types = array['image/jpeg']  -- строго .jpg (§5: один формат)
where id = 'event-photos';

-- ---------------------------------------------------------------------------
-- M-резервы · Cron-чистка брошенных резервов photos (uploaded=false)
-- ---------------------------------------------------------------------------
-- upload-url создаёт photos(uploaded=false) и резервирует слот лимита кадров до
-- фактической загрузки. Если гость «Переснял»/закрыл вкладку — строка осиротеет.
-- discard-photo даёт клиенту удалить резерв явно; эта задача подметает то, что
-- клиент не удалил: резервы старше 1 часа.
--
-- Удаляем строки НАПРЯМУЮ в SQL: объекта Storage у резерва ещё НЕТ (uploaded=false
-- означает, что PUT/confirm не прошёл) — чистить S3 не нужно, запрет «не удалять
-- из Storage в SQL» здесь не нарушается. Если же файл всё-таки был залит, но
-- confirm не вызывали, осиротевший объект подметёт purge-expired при истечении
-- события (retention) — это допустимо и редко.
--
-- Порог 1 час: подписанный upload-URL живёт ≤600 c (§5), так что через час резерв
-- заведомо протух и догрузиться уже не может.
create extension if not exists pg_cron;

do $cron$
begin
  -- Идемпотентность: снимаем прежнюю версию задачи, если есть.
  perform cron.unschedule(jobid)
  from cron.job
  where jobname = 'kadr-purge-stale-reservations';

  -- Ежечасно: удаляем резервы, не загруженные за час.
  perform cron.schedule(
    'kadr-purge-stale-reservations',
    '0 * * * *',
    $job$
      delete from public.photos
      where uploaded = false
        and taken_at < now() - interval '1 hour';
    $job$
  );
end
$cron$;

